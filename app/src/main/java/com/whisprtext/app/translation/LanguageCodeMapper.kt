    fun getNllbCodeForDisplayName(displayName: String): String {
        return SUPPORTED_LANGUAGES[displayName] ?: DEFAULT_NLLB_CODE
    }

    fun getDisplayNameForNllbCode(nllbCode: String): String {
        return SUPPORTED_LANGUAGES.entries.firstOrNull { it.value == nllbCode }?.key
            ?: Locale(nllbCode.take(3)).displayName
            ?: nllbCode
    }

    fun getNllbCodeForLocale(locale: Locale = Locale.getDefault()): String {
        val lang = locale.language.lowercase()
        return SUPPORTED_LANGUAGES.values.firstOrNull { it.startsWith(lang) } ?: DEFAULT_NLLB_CODE
    }

    fun getNllbCodeForLanguageCode(langCode: String): String {
        val lower = langCode.lowercase().trim()
        return when (lower) {
            "vi", "vie" -> "vie_Latn"
            "de", "deu" -> "deu_Latn"
            "fr", "fra" -> "fra_Latn"
            "zh", "zho", "zh-cn", "zh-hans" -> "zho_Hans"
            "es", "spa" -> "spa_Latn"
            "ja", "jpn" -> "jpn_Jpan"
            "hi", "hin" -> "hin_Deva"
            "en", "eng" -> "eng_Latn"
            else -> SUPPORTED_LANGUAGES[langCode]
                ?: SUPPORTED_LANGUAGES.values.firstOrNull { it.startsWith(lower) }
                ?: DEFAULT_NLLB_CODE
        }
    }

    fun isSupportedNllbCode(nllbCode: String): Boolean {
        return SUPPORTED_LANGUAGES.containsValue(nllbCode)
    }
